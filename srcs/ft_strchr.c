#include "get_next_line.h"

char	*ft_strchr(char const *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)&s[i]);
	return (0);
}
#include <stdio.h>
int main ()
{
	printf("%s\n", ft_strchr("abcdef", 'b'));
}
